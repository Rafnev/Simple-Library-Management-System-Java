    void insert() {
        String studentNameT = studentNameTextField.getText();
        String studentRollT = studentRollTextField.getText();
        String departmentT = jComboBox1.getSelectedItem().toString();
        String addressT = addressTextField.getText();
        String telephoneNoT = telephoneNoTextField.getText();
        
        if(!isInteger(studentRollT)){
            JOptionPane.showMessageDialog(null, "Student Roll must be an integer", "Error", JOptionPane.ERROR_MESSAGE , null);
        }
        
        String sql = "insert into StudentTable (StudentName, StudentRoll, TelephoneNumber, Address, Department) values ('" + studentNameT + "', '" + studentRollT + "', '" + telephoneNoT + "', '" + addressT + "', '" + departmentT  + "')";
        
        System.out.println(sql);
            
        try {
            st.executeUpdate(sql);
            
            JOptionPane.showMessageDialog(null, "Student Form Inserted", "Inserted", JOptionPane.OK_OPTION , null);
            
        } catch (SQLException ex) {
            Logger.getLogger(AddBookForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ERROR", "Error", JOptionPane.ERROR_MESSAGE , null);
        }
    }
    
